    private void bt_SaveActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_bt_SaveActionPerformed
        User updated = UserDB.getUser(Home.useid);
        String fullname = txt_FullName.getText();
        String email =  txt_Email.getText();
        if (fullname.isEmpty()){
            JOptionPane.showMessageDialog(null, "Full name must not empty.");
            return;
        }
        if (!email.matches(EMAIL)){
            JOptionPane.showMessageDialog(null, "Please enter email follow form [a-z]%@[a-z]%.[a-z]%");
            return;
        }
        updated.setEmail(email);
        updated.setFullName(fullname);
        
        String status = "";
        if (rbt_BT.isSelected()){
            status = "BT";
        }else if (rbt_KT.isSelected()){
            status = "KT";
        }else if (rbt_LT.isSelected()){
            status = "LT";
        }else if (rbt_SP.isSelected()){
            status = "SP";
        }
        updated.setStatus(status);
        String userright = (String)CB_UserRight.getSelectedItem();
        updated.setUserRight(Integer.parseInt(userright.trim()));
        UserDB.updateUser(updated);
        JOptionPane.showMessageDialog(null, "Update sucessful.");
    }//GEN-LAST:event_bt_SaveActionPerformed

