    private void getOldUser(){
        User olduser = UserDB.getUser(Home.useid);
        txt_FullName.setText(olduser.getFullName());
        txt_Email.setText(olduser.getEmail());
        String status = olduser.getStatus();
        if (status.equalsIgnoreCase("BT")){
            rbt_BT.setSelected(true);
        }else if (status.equalsIgnoreCase("LT")){
            rbt_LT.setSelected(true);
        }else if (status.equalsIgnoreCase("SP")){
            rbt_SP.setSelected(true);
        }else if (status.equalsIgnoreCase("KT")){
            rbt_KT.setSelected(true);
        }   
        CB_UserRight.getModel().setSelectedItem(olduser.getUserRight());
    }

